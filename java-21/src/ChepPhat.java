
public class ChepPhat {

	public static void main(String[] args) {
		// Bị phạt chép phạt 2000 lần "Em hứa sẽ làm bài tập đầy đủ"
		for (int i = 1; i <= 2000; i++) {
			System.out.println(i + ". Em hứa làm bài tập đầy đủ.");
		}
		
		for (int i = 2000; i > 0; i--) {
			System.out.println(i + ". Em hứa làm bài tập đầy đủ.");
		}
	}
	
}
