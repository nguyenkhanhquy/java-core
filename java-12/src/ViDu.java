
public class ViDu {

	public static void main(String[] args) {
		int a = 5;
		boolean b = false;
		System.out.println("-a => giá tri: " + (-a));
		System.out.println("+a => giá tri: " + (+a));
		System.out.println("!b => giá tri: " + (!b));
		System.out.println("----------");
		System.out.println("a = " + a);
		System.out.println("++a = " + (++a));
		System.out.println("a++ = " + (a++));
		System.out.println("a = " + a);
		System.out.println("--a = " + (--a));
		System.out.println("a-- = " + (a--));
		System.out.println("a = " + a);
	}
	
}
